interface LogoProps {
  className?: string
  size?: 'sm' | 'md' | 'lg'
}

const sizes = {
  sm: 'w-10 h-10',
  md: 'w-14 h-14',
  lg: 'w-20 h-20',
}

export default function Logo({ className = '', size = 'md' }: LogoProps) {
  return (
    <img
      src="/teknofest-logo.png"
      alt="Teknofest Logo"
      className={`${sizes[size]} object-contain ${className}`}
    />
  )
}
